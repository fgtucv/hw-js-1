const a = prompt("Який з напоїв він бажає придбати - Кава, Чай чи Сік")

switch (a){
    case "Кава" :
        alert(`В бажаєте замовити каву з вас 24грн`);
        break;

    case "Чай" :
        alert(`В бажаєте замовити чай з вас 15грн`);
        break;

    case "Скі" :
        alert(`В бажаєте замовити сік з вас 30грн`);
        break;
}